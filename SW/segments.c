//-----------------------------------------------------------------------------
// wr_mseg() writes 1-of-7-seg LCD memory (main)
//	seg 0-7 = a-g
//-----------------------------------------------------------------------------
void wr_mseg(U8 seg, U8 blank, U16 daddr){
	U8	mode;	// source array type 0,1,2
	U16	addr = daddr;
	U8*	iptr;
	U8	i;
	U8	j;

	switch(seg){
	case 0:	// a
		addr += SEGM_AX + (SEGM_AY*DROW);
		mode = SEGM_AO;
		iptr = *segm_a;
		break;

	case 1:	// b
		addr += SEGM_BX + (SEGM_BY*DROW);
		mode = SEGM_BO;
		iptr = segm_b;
		break;

	case 2:	// c
		addr += SEGM_CX + (SEGM_CY*DROW);
		mode = SEGM_CO;
		iptr = segm_c;
		break;

	case 3:	// d
		addr += SEGM_DX + (SEGM_DY*DROW);
		mode = SEGM_DO;
		iptr = *segm_d;
		break;

	case 4:	// e
		addr += SEGM_EX + (SEGM_EY*DROW);
		mode = SEGM_EO;
		iptr = *segm_e;
		break;

	case 5:	// f
		addr += SEGM_FX + (SEGM_FY*DROW);
		mode = SEGM_FO;
		iptr = *segm_f;
		break;

	case 6:	// g
		addr += SEGM_GX + (SEGM_GY*DROW);
		mode = SEGM_GO;
		iptr = *segm_g;
		break;

	case 7:	// dp
		addr += SEGM_PX + (SEGM_PY*DROW);
		mode = SEGM_PO;
		iptr = segm_p;
		break;

	default:
		mode = 0xff;
		break;
	}
	switch(mode){
	case 0:
		for(j=0; j<5; j++){
			for(i=0; i<3; i++){
				if(blank){
					main7[addr+i] &= ~(*iptr++);
				}else{
					main7[addr+i] |= *iptr++;
				}
			}
			addr += DROW;
		}
		break;

	case 1:
		for(j=0; j<19; j++){
			if(blank){
				main7[addr] &= ~(*iptr++);
			}else{
				main7[addr] |= *iptr++;
			}
			addr += DROW;
		}
		break;

	case 2:
		for(j=0; j<19; j++){
			for(i=0; i<2; i++){
				if(blank){
					main7[addr+i] &= ~(*iptr++);
				}else{
					main7[addr+i] |= *iptr++;
				}
			}
			addr += DROW;
		}
		break;

	case 3:
		for(j=0; j<4; j++){
			if(blank){
				main7[addr] &= ~(*iptr++);
			}else{
				main7[addr] |= *iptr++;
			}
			addr += DROW;
		}
		break;

	default:
		break;
	}
}

