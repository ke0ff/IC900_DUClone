//-----------------------------------------------------------------------------
// wr_mdigit() writes 7-seg digit to LCD memory (main)
//-----------------------------------------------------------------------------
void wr_mdigit(char digit, U16 daddr){
	U8	i = 0;
	U8	j;
	U16	addr = daddr;
	U8*	iptr;

	switch(digit){
	case '0':
		iptr = *digit0;
		break;

	case '1':
		iptr = *digit1;
		break;

	case '2':
		iptr = *digit2;
		break;

	case '3':
		iptr = *digit3;
		break;

	case '4':
		iptr = *digit4;
		break;

	case '5':
		iptr = *digit5;
		break;

	case '6':
		iptr = *digit6;
		break;

	case '7':
		iptr = *digit7;
		break;

	case '8':
		iptr = *digit8;
		break;

	case '9':
		iptr = *digit9;
		break;

	case 'S':
		iptr = *digitmS;
		break;

	case '.':
		break;

	default:
	case ' ':
		i = 0xff;
		break;
	}
	if(i){
		// blank digit
		for(j=0; j<NUMROWS_M; j++){
			for(i=0; i<MDWIDE; i++){
				main7[addr] = 0;
			}
			addr += DROW;
		}
	}else{
		if(digit == '.'){
			addr += NUMROWS_MDP * DROW;
			for(j=0; j<DIGITDP_Y; j++){
				main7[addr] |= segm_p[j];
				addr += DROW;
			}
		}else{
			if(digit == 'S'){
				addr += NUMROWS_MS * DROW;
				for(j=0; j<DIGITMS_Y; j++){
					for(i=0; i<MSDWIDE; i++){
						main7[addr+i] = *(iptr+i);
					}
					iptr = (iptr + MSDWIDE);
					addr += DROW;
				}
			}else{
				for(j=0; j<NUMROWS_M; j++){
					for(i=0; i<MDWIDE; i++){
						main7[addr+i] = *(iptr+i);
					}
					iptr = (iptr + MDWIDE);
					addr += DROW;
				}
			}
		}
	}
	return;
}

