//-----------------------------------------------------------------------------
// wr_msym2() writes a 2d array symbol to display mem - main
//-----------------------------------------------------------------------------
void wr_msym2(U8* sptr, U8 xlen, U8 ylen, U8 blank, U16 daddr, U8* mptr){
	U8	x;
	U8	y;
	U16	addr = daddr;
	U8*	iptr = sptr;

	for(y=0; y<ylen; y++){
		for(x=0; x<xlen; x++){
			if(blank){
				mptr[addr+x] &= ~(*(iptr+x));
			}else{
				mptr[addr+x] |= *(iptr+x);
			}
		}
		iptr = (iptr + xlen);
		addr += DROW;
	}
	return;
}

